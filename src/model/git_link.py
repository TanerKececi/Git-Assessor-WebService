from typing import Optional

class Git_Links():
    def __init__(
        self,
        rules : Optional[str] = None,
        link1 : Optional[str] = None,
        link2 : Optional[str] = None,
        link3 : Optional[str] = None,
        link4 : Optional[str] = None,
        link5 : Optional[str] = None
    ) -> None:
        self.rules = rules,
        self.link1 = link1
        self.link2 = link2
        self.link3 = link3
        self.link4 = link4
        self.link5 = link5
